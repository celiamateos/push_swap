#include "../libft.h"

int	ft_putun(unsigned int nbr, int *count)
{
	if (nbr / 10 != 0)
		ft_putun(nbr / 10, count);
	nbr = nbr % 10 + '0';
	*count += write (1, &nbr, 1);
	return (0);
}
